    /**
     * Verificar si el propietario ya existe
     */
    private function ownerExists(array $data): bool
    {
        // Si tiene DUI, buscar por DUI
        if (!empty($data['dui'])) {
            return Owner::where('dui', $data['dui'])->exists();
        }
        
        // Si tiene NIT, buscar por NIT
        if (!empty($data['tax_id'])) {
            return Owner::where('tax_id', $data['tax_id'])->exists();
        }
        
        // Si no tiene identificaciÃ³n, buscar por nombre exacto en la misma comunidad
        return Owner::where('name', $data['name'])
                    ->where('community', $data['community'])
                    ->whereNull('dui')
                    ->whereNull('tax_id')
                    ->exists();
    }

