    /**
     * Importar propietarios con manejo de errores
     */
    private function importOwners(array $owners): void
    {
        $imported = 0;
        $skipped = 0;
        $failed = 0;
        $errors = [];

        DB::beginTransaction();

        try {
            foreach ($owners as $index => $ownerData) {
                try {
                    // Validar que al menos tenga nombre
                    if (empty($ownerData['name'])) {
                        $skipped++;
                        $this->command->warn("âŠ˜ Fila " . ($index + 1) . ": Sin nombre - OMITIDO");
                        continue;
                    }

                    // Preparar datos limpios
                    $data = $this->prepareOwnerData($ownerData);

                    // Verificar si ya existe (por DUI o NIT)
                    if ($this->ownerExists($data)) {
                        $skipped++;
                        $this->command->warn("âŠ˜ {$data['name']}: Ya existe - OMITIDO");
                        continue;
                    }

                    // Crear propietario
                    Owner::create($data);
                    $imported++;

                    $this->command->info("âœ“ [{$imported}] {$data['name']} - {$data['community']}");

                } catch (\Exception $e) {
                    $failed++;
                    $errorMsg = "Fila " . ($index + 1) . ": {$ownerData['name']} - " . $e->getMessage();
                    $errors[] = $errorMsg;
                    
                    $this->command->error("âœ— Error: {$errorMsg}");
                    Log::error("ImportOwners Error: {$errorMsg}");
                }
            }

            DB::commit();

            // Resumen final
            $this->showSummary($imported, $skipped, $failed, $errors);

        } catch (\Exception $e) {
            DB::rollBack();
            $this->command->error("âŒ Error crÃ­tico: " . $e->getMessage());
            throw $e;
        }
    }

    /**
     * Preparar y limpiar datos del propietario
     */
    private function prepareOwnerData(array $data): array
    {
        // Detectar tipo de propietario automÃ¡ticamente
        $ownerType = 'natural';
        $dui = null;
        $taxId = null;

        if (!empty($data['dui_nit'])) {
            $cleaned = preg_replace('/[^0-9-]/', '', $data['dui_nit']);
            
            // Si tiene formato NIT (####-######-###-#)
            if (strlen(str_replace('-', '', $cleaned)) >= 14) {
                $ownerType = 'organization';
                $taxId = $cleaned;
            } 
            // Si tiene 9 dÃ­gitos es DUI
            elseif (strlen(str_replace('-', '', $cleaned)) == 9) {
                $ownerType = 'natural';
                $dui = str_replace('-', '', $cleaned); // Solo nÃºmeros
            }
        }

        return [
            'name' => trim($data['name']),
            'owner_type' => $ownerType,
            'dui' => $dui,
            'tax_id' => $taxId,
            'phone' => $this->cleanPhone($data['phone'] ?? null),
            'email' => $this->cleanEmail($data['email'] ?? null),
            'address' => !empty($data['address']) ? trim($data['address']) : null,
            'community' => $this->mapCommunity($data['community']),
        ];
    }

    /**
     * Limpiar nÃºmero de telÃ©fono (solo 8 dÃ­gitos)
     */
    private function cleanPhone(?string $phone): ?string
    {
        if (empty($phone)) return null;
        
        $cleaned = preg_replace('/\D/', '', $phone);
        return strlen($cleaned) == 8 ? $cleaned : null;
    }

    /**
     * Validar y limpiar email
     */
    private function cleanEmail(?string $email): ?string
    {
        if (empty($email)) return null;
        
        $email = trim($email);
        return filter_var($email, FILTER_VALIDATE_EMAIL) ? $email : null;
    }

